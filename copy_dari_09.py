# ============================================================
# 🔹 Fungsi bantu: Membagi teks panjang jadi beberapa chunk
# ============================================================
def chunk_text(text: str, tokenizer, max_input_length: int = 800, stride: int = 400) -> List[str]:
    inputs = tokenizer(
        text,
        return_tensors="pt",
        truncation=False,
        add_special_tokens=False,
    )
    input_ids = inputs["input_ids"][0]
    total_len = input_ids.size(0)
    chunks = []

    for i in range(0, total_len, stride):
        end = min(i + max_input_length, total_len)
        chunk_ids = input_ids[i:end]
        chunk_text = tokenizer.decode(chunk_ids, skip_special_tokens=True)
        chunks.append(chunk_text)
        if end == total_len:
            break
    return chunks

# ============================================================
# 🔹 Fungsi bantu: Ringkas 1 chunk
# ============================================================
def summarize_chunk(text: str, model, tokenizer, device="cpu",
                    max_input_length: int = 800, max_output_length: int = 100) -> str:
    inputs = tokenizer(
        text,
        return_tensors="pt",
        max_length=max_input_length,
        truncation=True,
        padding="max_length",
    ).to(device)

    with torch.no_grad():
        summary_ids = model.generate(
            inputs["input_ids"],
            num_beams=4,
            max_new_tokens=max_output_length,  # ✅ pakai max_new_tokens
            early_stopping=True,
            no_repeat_ngram_size=3,
            length_penalty=1.0,
        )

    return tokenizer.decode(summary_ids[0], skip_special_tokens=True)

# ============================================================
# 🔹 Fungsi utama: Ringkas teks panjang
# ============================================================
def summarize_long_text(
    text: str,
    model,
    tokenizer,
    device="cpu",
    max_input_length: int = 800,
    stride: int = 400,
    max_output_length: int = 100,
    num_sentences: int = 3,
) -> str:
    chunks = chunk_text(text, tokenizer, max_input_length=max_input_length, stride=stride)
    summaries = []

    for chunk in chunks:
        summary = summarize_chunk(
            chunk, model, tokenizer, device=device,
            max_input_length=max_input_length,
            max_output_length=max_output_length
        )
        summaries.append(summary)

    final_summary = " ".join(summaries).strip()
    sentences = final_summary.split(". ")
    if len(sentences) > num_sentences:
        sentences = sentences[:num_sentences]
    final_summary = ". ".join(sentences).strip()
    if not final_summary.endswith("."):
        final_summary += "."
    return final_summary

