def sent_tokenize_safe(text: str):
    try:
        from nltk.tokenize import sent_tokenize
        return sent_tokenize(text)
    except Exception:
        text = re.sub(r'\s+', ' ', str(text).strip())
        if not text: return []
        return re.split(r'(?<=[.!?])\s+(?=[A-Za-zÀ-ÖØ-öø-ÿ0-9"“‘])', text)

def norm(s): return re.sub(r"\s+"," ", str(s).lower().strip())

def is_lead_like(text, summ, k_sents=3):
    sents = sent_tokenize_safe(text)
    lead  = " ".join(sents[:k_sents]) if sents else str(text)
    return norm(summ) in norm(lead)

