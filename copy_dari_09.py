"""# training IndoBART"""

def lead_drop(text, p=0.3, max_drop=2):
    sents = sent_tokenize_safe(text)
    if random.random() < p and len(sents) > 0:
        k = min(len(sents), random.randint(1, max_drop))
        return " ".join(sents[k:])
    return text

def preprocess_train(batch):
    texts = [lead_drop(t, p=0.3, max_drop=2) for t in batch["text"]]
    mi = tokenizer(texts, max_length=MAX_INPUT_LEN, truncation=True)
    with tokenizer.as_target_tokenizer():
        labels = tokenizer(batch["summary"], max_length=MAX_TARGET_LEN, truncation=True)
    mi["labels"] = labels["input_ids"]
    return mi

def preprocess_valid(batch):
    mi = tokenizer(batch["text"], max_length=MAX_INPUT_LEN, truncation=True)
    with tokenizer.as_target_tokenizer():
        labels = tokenizer(batch["summary"], max_length=MAX_TARGET_LEN, truncation=True)
    mi["labels"] = labels["input_ids"]
    return mi

tokenized_train = ds["train"].map(preprocess_train, batched=True, remove_columns=ds["train"].column_names)
tokenized_valid = ds["validation"].map(preprocess_valid, batched=True, remove_columns=ds["validation"].column_names)
